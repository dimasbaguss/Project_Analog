    private void pilihJurusanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pilihJurusanActionPerformed
        //menentukan harga sesuai jurusan
        if (pilihJurusan.getSelectedItem().equals("Pilih Jurusan")) {     
            isiHarga.setText("");
            isiNomorBis.setText("");
        }
        else if(pilihJurusan.getSelectedItem().equals("Pontianak - Ng. Pinoh")) {
            royal = 210000;
            royalS = "1A";
            eksekutif = 170000;
            eksekutifS = "1B";
        }
         else if(pilihJurusan.getSelectedItem().equals("Pontianak - Sintang")) {
            royal = 200000;
            royalS = "2A";
            eksekutif = 150000;
            eksekutifS = "2B";
        }
         else if(pilihJurusan.getSelectedItem().equals("Pontianak - Sekadau")) {
            royal = 170000;
            royalS = "3A";
            eksekutif = 140000;
            eksekutifS = "3B";
        }
         else if(pilihJurusan.getSelectedItem().equals("Pontianak - Sanggau")) {
            royal = 100000;
            royalS = "4A";
            eksekutif = 75000;
            eksekutifS = "4B";
        }
         else if(pilihJurusan.getSelectedItem().equals("Pontianak - Tayan")) {
            royal = 130000;
            royalS = "5A";
            eksekutif = 100000;
            eksekutifS = "5B";
        }
         else if(pilihJurusan.getSelectedItem().equals("Pontianak - Ketapang")) {
            royal = 240000;
            royalS = "6A";
            eksekutif = 200000;
            eksekutifS = "6B";
        }
        //menampilkan jurusan yang dipilih ke txtfieldtampilJurusan
        String selectedValue = pilihJurusan.getSelectedItem().toString();
        tampilJurusan.setText(selectedValue);
    }//GEN-LAST:event_pilihJurusanActionPerformed

