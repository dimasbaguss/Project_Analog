    private void isiJumlahTiketKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_isiJumlahTiketKeyReleased
        // jumlah tiket yg akan dibeli (akan langsung ditampilkan di total)
        a = Integer.valueOf(isiHarga.getText());
        b = Integer.valueOf(isiJumlahTiket.getText());
        c = a * b;
        isiTotal.setText("" + c);
    }//GEN-LAST:event_isiJumlahTiketKeyReleased

    private void isiBayarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_isiBayarKeyReleased
         // uang yang dibayar pembeli (akan langsung ditampilkan di total)
        a = Integer.valueOf(isiBayar.getText());
        b = Integer.valueOf(isiTotal.getText());
        c = a - b;
        isiKembalian.setText("" + c);
    }//GEN-LAST:event_isiBayarKeyReleased

